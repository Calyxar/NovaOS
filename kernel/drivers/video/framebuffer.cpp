// VGA fallback
static uint16_t* vga_buf = (uint16_t*)0xB8000;
static int vga_x = 0, vga_y = 0;

static void vga_print(const char* str, uint8_t color) {
    for (int i = 0; str[i]; i++) {
        char c = str[i];
        if (c == '\n') { vga_x = 0; vga_y++; continue; }
        if (vga_x >= 80) { vga_x = 0; vga_y++; }
        vga_buf[vga_y * 80 + vga_x++] = 0x0F00 | (uint8_t)c;
    }
}

void Framebuffer::init() {
    vga_x = vga_y = 0;
    vesa_mode = false;
    fb.addr = nullptr;
}

void Framebuffer::init_vesa(uint32_t* addr, uint32_t w, uint32_t h,
                             uint32_t pitch, uint8_t bpp) {
    fb.addr  = addr;
    fb.width = w; fb.height = h;
    fb.pitch = pitch; fb.bpp = bpp;
    vesa_mode = true;
    cur_x = 8; cur_y = 8;
}

void Framebuffer::put_pixel(int x, int y, uint32_t color) {
    if (!fb.addr || x < 0 || y < 0 || (uint32_t)x >= fb.width || (uint32_t)y >= fb.height) return;
    uint32_t* row = (uint32_t*)((uint8_t*)fb.addr + y * fb.pitch);
    row[x] = color;
}

void Framebuffer::clear(uint32_t color) {
    if (!vesa_mode) {
        for (int i = 0; i < 80*25; i++) vga_buf[i] = 0x0F00 | ' ';
        vga_x = vga_y = 0; return;
    }
    for (uint32_t y = 0; y < fb.height; y++) {
        uint32_t* row = (uint32_t*)((uint8_t*)fb.addr + y * fb.pitch);
        for (uint32_t x = 0; x < fb.width; x++) row[x] = color;
    }
    cur_x = 8; cur_y = 8;
}

void Framebuffer::draw_rect(int x, int y, int w, int h, uint32_t color) {
    for (int row = y; row < y + h; row++)
        for (int col = x; col < x + w; col++)
            put_pixel(col, row, color);
}

void Framebuffer::draw_circle(int cx, int cy, int r, uint32_t color) {
    for (int y = -r; y <= r; y++)
        for (int x = -r; x <= r; x++)
            if (x*x + y*y <= r*r)
                put_pixel(cx+x, cy+y, color);
}

void Framebuffer::draw_rounded_rect(int x, int y, int w, int h, int radius, uint32_t color) {
    if (radius > w/2) radius = w/2;
    if (radius > h/2) radius = h/2;
    int r2 = radius * radius;

    for (int row = 0; row < h; row++) {
        for (int col = 0; col < w; col++) {
            // Determine which corner zone (if any) this pixel falls in
            int cx = -1, cy = -1; // corner center, relative to this pixel
            if (col < radius && row < radius) { cx = radius; cy = radius; }
            else if (col >= w-radius && row < radius) { cx = w-radius-1; cy = radius; }
            else if (col < radius && row >= h-radius) { cx = radius; cy = h-radius-1; }
            else if (col >= w-radius && row >= h-radius) { cx = w-radius-1; cy = h-radius-1; }

            if (cx >= 0) {
                int dx = col - cx, dy = row - cy;
                if (dx*dx + dy*dy > r2) continue; // outside the rounded corner, skip
            }
            put_pixel(x + col, y + row, color);
        }
    }
}

void Framebuffer::draw_rect_round_br(int x, int y, int w, int h, int radius, uint32_t color) {
    // Rounds only the bottom-right corner; all other edges stay sharp.
    // Useful for panels anchored to a screen edge (sidebar, topbar, etc).
    if (radius > w/2) radius = w/2;
    if (radius > h/2) radius = h/2;
    int r2 = radius * radius;
    int cx = w - radius - 1, cy = h - radius - 1;

    for (int row = 0; row < h; row++) {
        for (int col = 0; col < w; col++) {
            if (col >= w-radius && row >= h-radius) {
                int dx = col - cx, dy = row - cy;
                if (dx*dx + dy*dy > r2) continue;
            }
            put_pixel(x + col, y + row, color);
        }
    }
}

void Framebuffer::draw_line(int x0, int y0, int x1, int y1, uint32_t color) {
    int dx = x1-x0 < 0 ? x0-x1 : x1-x0;
    int dy = y1-y0 < 0 ? y0-y1 : y1-y0;
    int sx = x0 < x1 ? 1 : -1;
    int sy = y0 < y1 ? 1 : -1;
    int err = dx - dy;
    while (true) {
        put_pixel(x0, y0, color);
        if (x0 == x1 && y0 == y1) break;
        int e2 = 2 * err;
        if (e2 > -dy) { err -= dy; x0 += sx; }
        if (e2 <  dx) { err += dx; y0 += sy; }
    }
}

static inline uint32_t blend_half(uint32_t fg, uint32_t bg) {
    uint32_t fr=(fg>>16)&0xFF, fgc=(fg>>8)&0xFF, fb2=fg&0xFF;
    uint32_t br=(bg>>16)&0xFF, bgc=(bg>>8)&0xFF, bb=bg&0xFF;
    uint32_t r=(fr+br)/2, g=(fgc+bgc)/2, b=(fb2+bb)/2;
    return (r<<16)|(g<<8)|b;
}

void Framebuffer::draw_char(char c, int x, int y, uint32_t color) {
    if ((unsigned char)c < 32 || (unsigned char)c > 127) return;
    const uint8_t* glyph = font8x16[c - 32];

    // First pass: plot all solid pixels
    bool lit[16][8] = {};
    for (int row = 0; row < 16; row++) {
        for (int col = 0; col < 8; col++) {
            if (glyph[row] & (0x80 >> col)) {
                lit[row][col] = true;
                put_pixel(x + col, y + row, color);
            }
        }
    }

    // Second pass: lightly blend diagonal neighbors of lit pixels that are
    // themselves unlit, softening jagged edges without needing real alpha data
    for (int row = 0; row < 16; row++) {
        for (int col = 0; col < 8; col++) {
            if (lit[row][col]) continue;
            int neighbors = 0;
            if (row>0 && col>0 && lit[row-1][col-1]) neighbors++;
            if (row>0 && col<7 && lit[row-1][col+1]) neighbors++;
            if (row<15 && col>0 && lit[row+1][col-1]) neighbors++;
            if (row<15 && col<7 && lit[row+1][col+1]) neighbors++;
            if (neighbors >= 2) {
                // Read existing background pixel color isn't trivial without a
                // framebuffer read-back helper, so approximate with a fixed
                // dark blend toward the glyph color for a soft anti-aliased look
                uint32_t soft = blend_half(color, 0x000000);
                put_pixel(x + col, y + row, soft);
            }
        }
    }
}

void Framebuffer::scroll() {
    if (!vesa_mode) return;
    uint32_t line_h = 10;
    uint32_t copy_bytes = fb.pitch * (fb.height - line_h);
    uint8_t* dst = (uint8_t*)fb.addr;
    uint8_t* src = (uint8_t*)fb.addr + fb.pitch * line_h;
    for (uint32_t i = 0; i < copy_bytes; i++) dst[i] = src[i];
    // Clear last line
    for (uint32_t y = fb.height - line_h; y < fb.height; y++) {
        uint32_t* row = (uint32_t*)((uint8_t*)fb.addr + y * fb.pitch);
        for (uint32_t x = 0; x < fb.width; x++) row[x] = 0x01020F; // dark bg
    }
    cur_y -= line_h;
}

void Framebuffer::print_at(const char* str, int x, int y, uint32_t color) {
    int px = x;
    for (int i = 0; str[i]; i++) {
        if (str[i] == '\n') { px = x; y += 10; continue; }
        draw_char(str[i], px, y, color);
        px += 8;
    }
}

void Framebuffer::print(const char* str, uint32_t color) {
    if (!vesa_mode) { vga_print(str, 0x0F); return; }
    for (int i = 0; str[i]; i++) {
        char c = str[i];
        if (c == '\n') { cur_x = 8; cur_y += 10; }
        else if (c == '\b') { if (cur_x > 8) cur_x -= 8; }
        else {
            if (cur_x + 8 >= (int)fb.width) { cur_x = 8; cur_y += 10; }
            if (cur_y + 10 >= (int)fb.height) scroll();
            draw_char(c, cur_x, cur_y, color);
            cur_x += 8;
        }
    }
}

void Framebuffer::set_cursor(int x, int y) { cur_x = x; cur_y = y; }
Framebuffer::Info& Framebuffer::get_info() { return fb; }
int Framebuffer::get_cursor_x() { return cur_x; }
int Framebuffer::get_cursor_y() { return cur_y; }
